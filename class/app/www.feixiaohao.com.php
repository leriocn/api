	/**
	 * [getMarkets 获取市场行情]
	 * @return [type] [description]
	 */
	private function getMarkets()
	{
		//decentraland bitcoin
		@$coin = $this->param->Coin;
		$coin = $this->getCoinName($coin);
		if(empty($coin) == false)
		{
			$url = 'http://www.feixiaohao.com/currencies/'.$coin;
			$reg = array(
						'排名' =>	array('td:eq(0)','text'),
						'交易平台' =>	array('td:eq(1)','text'),
						'交易对' =>	array('td:eq(2)','text'),
						'价格' =>	array('td:eq(3)','text'),
						'成交量' =>	array('td:eq(4)','text'),
						'占比' =>	array('td:eq(5)','text'),
						'更新时间' => array('td:eq(6)','text'),
						);
			$rang = '#table tbody tr';
			//使用curl抓取源码并以utf-8编码格式输出
			$qy = new QueryList($url,$reg,$rang,'curl','utf-8');
			$arr = $qy->jsonArr;
			$arrCount = count($arr,0);

			$topCount = $this->param->top;
			if($topCount > $arrCount)
			{
				$topCount = $arrCount;
			}

			if($topCount > 0)
			{
				$arr = array_slice($arr,0,$topCount,true);
			}

			if($topCount > 0)
			{
				$this->success = 'yes';
				$this->resultData = $arr;
			}
			else
			{
				$this->success = 'no';
				$this->information = '没有获取到数据！';
			}
		}
		else
		{
			$this->success = 'no';
			$this->information = '币种不可为空！';
		}
	}


